require 'weather'
require 'airport'

describe Weather do

  context 'when the weather is stormy' do
    it 'returns true' do
      weather_forecaster = Weather.new
      allow(weather_forecaster).to receive(:weather_generator) {:stormy}
      expect(weather_forecaster.is_stormy?).to be true
    end
  end

  context 'when the weather is sunny' do
    it 'returns false' do
      weather_forecaster = Weather.new
      allow(weather_forecaster).to receive(:weather_generator) {:sunny}
      expect(weather_forecaster.is_stormy?).to be false
    end
  end

end
